package pbs.base.pojo.vo;

public class GPSScope {
	private Double lng;
	private Double lat;
	private Double lng_low;
	private Double lng_high;
	private Double lat_low;
	private Double lat_high;
	
	public Double getLng() {
		return lng;
	}
	public void setLng(Double lng) {
		this.lng = lng;
	}
	public Double getLat() {
		return lat;
	}
	public void setLat(Double lat) {
		this.lat = lat;
	}
	public Double getLng_low() {
		return lng_low;
	}
	public void setLng_low(Double lng_low) {
		this.lng_low = lng_low;
	}
	public Double getLng_high() {
		return lng_high;
	}
	public void setLng_high(Double lng_high) {
		this.lng_high = lng_high;
	}
	public Double getLat_low() {
		return lat_low;
	}
	public void setLat_low(Double lat_low) {
		this.lat_low = lat_low;
	}
	public Double getLat_high() {
		return lat_high;
	}
	public void setLat_high(Double lat_high) {
		this.lat_high = lat_high;
	}
	
}
